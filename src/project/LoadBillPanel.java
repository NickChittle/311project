  private void loadBillEvent() {
    int index = billsList.getSelectedIndex();
    if (index != -1) {
      DBBill bill = billsListModel.getElementAt(index);
      mainFrame.loadBill(bill.getId());
    }
  }
  
  public void readBillsFromDB() {
    DatabaseManager db = new DatabaseManager();
    List<DBBill> bills = db.getBills();
    billsListModel.clear();
    for (DBBill bill : bills) {
      billsListModel.addElement(bill);
    }
  }

